    /**
     * Reads the user input, and convert the weight, then set the result in the
     * output label
     */
    public void convertWeight() {
        String result = "";
        try {
            // read from the textfield
            double input = Double.parseDouble(intputTF.getText());

            // conversion
            if (kg2LbRB.isSelected())
                result = String.format("%.2f", kgToLb(input));
            else
                result = String.format("%.2f", lbToKg(input));

            // write to the result label
            outputL.setText(result);
        } catch (Exception e) {

        }
    }

